void solve() {
	string s{};
	cin >> s;
	string res{};
	for (const char c: s) {
		if (!(c == 'a' || c == 'e' || c == 'i' || c == 'o' || c == 'u')) {
			res += c;
		}
	}
	cout << res << '\n';
}

